     /**
     * Cadastra um novo emprestimo na tabela "emprestimos"
     */
     protected void cadastrarEmprestimo(){
        Emprestimo objEmprestimo = new Emprestimo();
        
        String nome = txtNomeCliente.getText();
        String cpf = txtCpf.getText();
        String nomeJogo = txtNomeJogo.getText();
        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataEmp = LocalDate.parse(txtDataEmprestimo.getText(), formatter);
        LocalDate devolucao = LocalDate.parse(txtDevolucao.getText(), formatter);

        
        objEmprestimo.setFkCliente(clienteSelecionado);
        objEmprestimo.setFkJogo(jogoSelecionado);
        objEmprestimo.setNomeCliente(nome);
        objEmprestimo.setCpf(cpf);
        objEmprestimo.setTituloJogo(nomeJogo);
        objEmprestimo.setDataEmp(dataEmp);
        objEmprestimo.setDevolucao(devolucao);
        
        
        EmprestimoDAO emprestimoDAO = new EmprestimoDAO();
        emprestimoDAO.inserir(objEmprestimo);
        
        // Mostra mensagem de Sucesso
        JOptionPane.showMessageDialog(
            this,
            "Cadastro bem-sucedido!",
            "Sucesso",
            JOptionPane.INFORMATION_MESSAGE
        );
        
    }


    
