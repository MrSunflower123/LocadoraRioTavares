    //Valida se os campos não estão vazios e no formato correto
    private void validarCampos() throws Exception {
        String nomeCliente = txtNomeCliente.getText();
        String cpf = txtCpf.getText();
        String nomeJogo = txtNomeJogo.getText();
        String dataEmpTexto = txtDataEmprestimo.getText().trim();
        String devolucaoTexto = txtDevolucao.getText().trim();
        
       
        
        
        
        //NOME DO CLIENTE
        //Nome do cliente não pode estar vazio
        if (nomeCliente.isEmpty()){
            throw new Exception ("Campo Nome do cliente não pode estar vazio.");
        }
        
        
        //CPF
        //CPF não pode estar vazio
        if (cpf.isEmpty()) {
            throw new Exception ("CPF não pode ser vazio.");
        }
        
        
        //NOME DE JOGO
        //Nome de jogo não pode estar vazio
        if (nomeJogo.isEmpty()){
           throw new Exception ("Campo Nome do jogo aceita somente letras.");
        }
       
        
        //DATA DE EMPRÉSTIMO
        //Data de empréstimo não pode estar vazio
        if (dataEmpTexto.isEmpty()){
           throw new Exception ("Campo Data do empréstimo não pode estar vazio.");
        }
        
        // Data de empréstimo precisa estar no formato DD/MM/AAAA
        if (!dataEmpTexto.matches("\\d{2}/\\d{2}/\\d{4}")) {
        throw new Exception("Data de empréstimo deve estar no formato DD/MM/AAAA. EX: 01/01/2000");
        }
        
        //Data de empréstimo precisa existir no calendário
        try {
        LocalDate.parse(dataEmpTexto, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        }catch (Exception e) {
        throw new Exception("Data de empréstimo inexistente no calendário");
        }
        
        
        //DATA DEVOLUÇÃO
        //Data devolução não pode estar vazio
        if (devolucaoTexto.isEmpty()) {
        throw new Exception("Campo Data de Devolução não pode estar vazio.");
        }
        
        // Data devolução precisa estar no formato DD/MM/AAAA
        if (!devolucaoTexto.matches("\\d{2}/\\d{2}/\\d{4}")) {
        throw new Exception("Data de devolução deve estar no formato DD/MM/AAAA. EX: 01/01/2025");
        }
        
        //Data devolução precisa existir no calendário
        try {
        LocalDate.parse(devolucaoTexto, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        } catch (Exception e) {
        throw new Exception("Data de devolução inexistente no calendário.");
        }
        }
    
    
