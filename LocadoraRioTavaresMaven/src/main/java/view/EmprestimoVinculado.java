    /**
        * Preenche tblEmprestimos com os dados da base de dados
        * @param lista 
        */
        protected void preencherTabela(List<Emprestimo> lista) {

        DefaultTableModel modelo = (DefaultTableModel) tblEmprestimos.getModel();

        // Remove todas as linhas atuais
        modelo.setRowCount(0);

       
        for (Emprestimo emp: lista) {
            modelo.addRow(new Object[]{
                emp.getId(),
                emp.getFkCliente(),
                emp.getFkJogo(),
                emp.getNomeCliente(),
                emp.getCpf(),
                emp.getTituloJogo(),
                emp.getDataEmp(),
                emp.getDevolucao(),
            });
        }
    }

    
    /**
     * Preenche tblEmprestimos de acordo com o filtro selecionado
     */
     protected void aplicarFiltro(){
         String texto = txtPesquisar.getText();
         int filtro = cbxFiltros.getSelectedIndex();
         EmprestimoDAO empDAO = new EmprestimoDAO();
         List <Emprestimo> lista;
         
         if (texto.length() >= 1){
             lista = empDAO.listaComFiltro(filtro, texto);
            
         }
         
         else {
             lista = empDAO.listar();
         }
         
         preencherTabela(lista);
           
     }
    
     
     /**
      * Mostra os emprestimo feitos pelo CPF digitado
      */
     protected void listarEmprestimos(){
         String cpf = clientePassado.getCpf();
         
         cbxFiltros.setSelectedIndex(2);
         txtPesquisar.setText(cpf);
         
         
         aplicarFiltro();
     }
    
    /**
     * Mapeia os atalhos para a tela
     */ 
     protected void mapearTeclas(){
        
        
        // Alt + 1
        lblPesquisar.setDisplayedMnemonic(KeyEvent.VK_1);
        
        // Alt + V
        btnVoltar.setMnemonic(KeyEvent.VK_V);                                   
    }
    
    
